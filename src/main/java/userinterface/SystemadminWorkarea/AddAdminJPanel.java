    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        int eid ;
        String ename;
        String pw;
        
        int role;
        try{
            eid = Integer.parseInt(txtid.getText());
            ename = txtname.getText();
            pw = txtpw.getText();
            String se = (String)comR.getSelectedItem();
           if(se.equals("police admin")){
               role  = 0;
           }else if(se.equals("fireman admin")){
                role  = 1;
           }else if(se.equals("hospital admin")){
                role  = 2;
           }else{
               role =3;
           }

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Please check prize, size", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        OrganizationAdmin ad = new OrganizationAdmin(eid, ename, pw ,role);
        this.admin.createAdmin(ad);
        JOptionPane.showMessageDialog(this, "admin  added!", "Info", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_saveActionPerformed


