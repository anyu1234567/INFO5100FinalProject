    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        int id ; 
        String name;
        String pw;
        int isindi;
       String city;
       String email;
        try{
           id = Integer.parseInt(txtvolunterid.getText());
           name = txtUsername.getText();
           pw = txtPassword.getText();
           city = txtCity.getText();
           email =txtEmail.getText();
            String avol  = (String)cindi.getSelectedItem();
           if(avol.equals("individual")){
               isindi =1;
           }else{
               isindi =0;
           }
               
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Please check prize, size", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        this.vol = new Voluntary(id, name, pw, isindi, city, email);
        vol.update();
        JOptionPane.showMessageDialog(this, "voluntary  updated!", "Info", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jToggleButton1ActionPerformed


