    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        int row = tblRequest.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Disaster d = (Disaster)tblRequest.getValueAt(row, 0);   
        vol.chooseDisaster(d.getDisaster_id());
        JOptionPane.showMessageDialog(null, "assign disaster success", "Warning", JOptionPane.WARNING_MESSAGE);
        refreshDisaster();
        refreshAcceptedDisaster();
    }//GEN-LAST:event_btnAssignActionPerformed

