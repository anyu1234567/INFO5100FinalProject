    public void refreshDisaster() {
          int rowCount = tblRequest.getRowCount();
        DefaultTableModel model = (DefaultTableModel) tblRequest.getModel();
        for(int i=rowCount-1;i>=0;i--){
            model.removeRow(i);
        }
        
        for (Disaster di:vol.queryDisaster()) {
                
               Object row[] = new Object[5];
                row[0] = di;
                row[1] = di.getDisaster_name();
                row[2] = di.getDisaster_level();
                row[3] = di.getAllowed_voluntary();
                row[4] = di.getMax_voluntary();

                model.addRow(row);
           
            
            
        }
    }
    public void refreshAcceptedDisaster() {
          int rowCount = tblAccepted.getRowCount();
        DefaultTableModel model = (DefaultTableModel) tblAccepted.getModel();
        for(int i=rowCount-1;i>=0;i--){
            model.removeRow(i);
        }
        
        for (Disaster di:vol.queryDisasterByVol()) {
                
               Object row[] = new Object[5];
                row[0] = di;
                row[1] = di.getDisaster_name();
                row[2] = di.getDisaster_level();
                row[3] = di.getAllowed_voluntary();
                row[4] = di.getMax_voluntary();

                model.addRow(row);
           
            
            
        }
    }
